// Function to apply gradient backgrounds based on class names
function applyGradientBackground() {
    const elements = document.querySelectorAll('[class*="bg-gradient-"], [class*="from-"], [class*="to-"], [class*="via-"]');

    elements.forEach(elem => {
        const gradientClasses = Array.from(elem.classList).filter(c => c.startsWith('bg-gradient-'));
        const fromClasses = Array.from(elem.classList).filter(c => c.startsWith('from-'));
        const toClasses = Array.from(elem.classList).filter(c => c.startsWith('to-'));
        const viaClasses = Array.from(elem.classList).filter(c => c.startsWith('via-'));

        // Check if the element has any gradient class
        if (gradientClasses.length > 0) {
            // Build gradient style
            const gradientDirection = gradientClasses[0]; // Take the first found gradient class
            const fromColor = fromClasses.length > 0 ? fromColors[fromClasses[0]] : 'transparent'; // Default to transparent if no from class
            const toColor = toClasses.length > 0 ? toColors[toClasses[0]] : 'transparent'; // Default to transparent if no to class
            const viaColor = viaClasses.length > 0 ? viaColors[viaClasses[0]] : 'transparent'; // Default to transparent if no via class

            // Set the background gradient
            elem.style.background = gradientColors[gradientDirection].replace('var(--tw-gradient-stops)', `${fromColor}, ${viaColor}, ${toColor}`);
        }
    });
}

// Apply gradient backgrounds on DOMContentLoaded
document.addEventListener('DOMContentLoaded', applyGradientBackground);
